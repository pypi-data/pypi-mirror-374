from sardana.pool.test.util import ActionEvent


def test_pseudo_motor_motion_maintaining_sibling_position(gap01, offset01):
    motion_event = ActionEvent(gap01)
    gap01.position = 10
    motion_event.started.wait(1)
    motion_event.done.wait(1)
    assert offset01.position.value == 0